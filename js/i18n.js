// Active language
let _lang = localStorage.getItem('steeg_lang') || 'zh';

function t(key) {
  return (I18N[_lang] || I18N.zh)[key] || key;
}

function setLang(lang) {
  _lang = lang;
  localStorage.setItem('steeg_lang', lang);
  document.documentElement.lang = lang === 'zh' ? 'zh-TW' : 'en';
  applyLang();
}

function getLang() { return _lang; }

function applyLang() {
  // Update all [data-i18n] elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });
  // Toggle zh/en text blocks
  document.querySelectorAll('[data-lang]').forEach(el => {
    el.style.display = el.dataset.lang === _lang ? '' : 'none';
  });
  // Update lang toggle button
  const btn = document.getElementById('lang-toggle');
  if (btn) btn.textContent = _lang === 'zh' ? 'EN' : '中';
}
